# 33. Convert to Uppercase/Lowercase
def to_uppercase(s):
    """Converts a string to uppercase."""
    return s.upper()

def to_lowercase(s):
    """Converts a string to lowercase."""
    return s.lower()

# Example:
convert_string = input("Enter a string for case conversion: ")
print(f"Uppercase: {to_uppercase(convert_string)}, Lowercase: {to_lowercase(convert_string)}")

